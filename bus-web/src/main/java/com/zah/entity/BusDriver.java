package com.zah.entity;

public class BusDriver {
	private int driver_id;
	private String driver_number;
	private String driver_name;
	private int driver_sex;
	private String driver_phone;
	private String driver_identity;

	public int getDriver_id() {
		return driver_id;
	}

	public void setDriver_id(int driver_id) {
		this.driver_id = driver_id;
	}

	public String getDriver_number() {
		return driver_number;
	}

	public void setDriver_number(String driver_number) {
		this.driver_number = driver_number;
	}

	public String getDriver_name() {
		return driver_name;
	}

	public void setDriver_name(String driver_name) {
		this.driver_name = driver_name;
	}

	public int getDriver_sex() {
		return driver_sex;
	}

	public void setDriver_sex(int driver_sex) {
		this.driver_sex = driver_sex;
	}

	public String getDriver_phone() {
		return driver_phone;
	}

	public void setDriver_phone(String driver_phone) {
		this.driver_phone = driver_phone;
	}

	public String getDriver_identity() {
		return driver_identity;
	}

	public void setDriver_identity(String driver_identity) {
		this.driver_identity = driver_identity;
	}

}
